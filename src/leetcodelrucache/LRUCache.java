class LRUCache {

    private int capacity;
    private Cache head;
    private Cache tail;
    private HashMap<Integer, Cache> map = new HashMap<>();


    public LRUCache(int capacity) {
        this.capacity = capacity;
        head = new Cache(-1, -1);
        tail = new Cache(-1, -1);
        head.next = tail;
        tail.prev = head;
    }

    public int get(int key) {
        if (!map.containsKey(key)) {
            return -1;
        }
        Cache current = map.get(key);
        moveToTail(current);
        return current.val;
    }

    public void put(int key, int value) {
        //å­˜åœ¨
        if (get(key) != -1) {
            Cache cache = map.get(key);
            cache.val = value;
            return;
        }
        //ä¸å­˜åœ¨
        if (map.size() == capacity) {
            map.remove(map.remove(head.next.key));
            head.next.next.prev=head;
            head.next = head.next.next;

        }
        Cache cache = new Cache(key, value);
        tail.prev.next = cache;
        cache.prev = tail.prev;
        tail.prev = cache;
        cache.next = tail;
        map.put(key, cache);

    }

    public void moveToTail(Cache current) {
        if(current.next==tail){
            return;
        }
        current.prev.next = current.next;
        current.next.prev = current.prev;
        tail.prev.next = current;
        current.prev = tail.prev;
        tail.prev = current;
        current.next = tail;
    }

    private class Cache {
        public int key;
        public int val;
        public Cache prev;
        public Cache next;

        public Cache(int key, int val) {
            this.key = key;
            this.val = val;
        }
    }

    public static void main(String[] args) {
        LRUCache lruCache = new LRUCache(3);
        lruCache.put(1, 1);
        lruCache.put(2, 2);
        lruCache.put(3, 3);
        lruCache.put(4, 4);
        System.out.println(lruCache.get(4));
        System.out.println(lruCache.get(3));
        System.out.println(lruCache.get(2));
        System.out.println(lruCache.get(1));
        lruCache.put(5, 5);
        System.out.println(lruCache.get(1));
        System.out.println(lruCache.get(2));
        System.out.println(lruCache.get(3));
        System.out.println(lruCache.get(4));
        System.out.println(lruCache.get(5));

        System.out.println("----");
    }
}

